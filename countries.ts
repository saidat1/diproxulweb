export interface Subject {
  id: string; // Unique identifier for the subject
  name: string; // Name of the subject
  slug: string; // Unique slug for the subject
  code: string; // Code for the subject
  shortName?: string; // Optional: Short name for the subject
  category: SubjectCategory; // Category of the subject
  type: SubjectType; // Type of the subject (Theory, Practical, Both)
  passingMarks?: number; // Optional: Passing marks for the subject
  totalMarks?: number; // Optional: Total marks for the subject

  departmentName: string; // Name of the department
  departmentId: string; // Optional foreign key for the department
  department: Department; // Optional department relation

  createdAt: string; // Creation timestamp
  updatedAt: string; // Update timestamp

  isActive: boolean; // Whether the subject is active
  isOptional: boolean; // Whether the subject is optional
  hasPractical: boolean; // Whether the subject has a practical component
  hasTheory: boolean; // Whether the subject has a theory component
  labRequired: boolean; // Whether the subject requires a lab
}

